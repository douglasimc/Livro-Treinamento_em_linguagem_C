/* ponteirocons.c */
/* Mostra ponteiros constantes */
#include <stdio.h>
#include <stdlib.h>

int main()
{
    int i,j,k;

    printf("Endereço de i = %p\n", &i); /* %p para ponteiros */
    printf("Endereço de j = %p\n", &j);
    printf("Endereço de k = %p\n", &k);

    system("PAUSE");
    return 0;
}
